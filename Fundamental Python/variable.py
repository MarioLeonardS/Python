# Variable adalah sebuah nama atau identifikasi yang diberikan untuk menyimpan suatu nilai data dalam memori komputer
email = "dada@example.com" # Email adalah variable dan "dada@example.com" adalah data yang tersimpan di dalam variable
print(email) # Ini akan menjadi "dada@example.com"
print(email)
print(email)

