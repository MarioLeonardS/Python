#Belajar String
#Untuk membuat string harus menggunakan tanda kutip "" dan ''
Nama = "Mario Leonard Salim"
Umur = '16'
print(Nama, Umur)

#Tipe data String atau teks juga bisa menggabungkan 2 teks yang terpisah menggunakan +
#Contoh
nama_depan = "Mario"
nama_belakang = "Leonard"
nama_lengkap = nama_depan + nama_belakang
print(nama_lengkap)

#Jika ingin menambahkan spasi, tambahkan teks kosong " "
nama_lengkap = nama_depan + " " + nama_belakang
print(nama_lengkap)