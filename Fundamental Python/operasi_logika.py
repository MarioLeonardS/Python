# Operator Logika
# Materi Operator Logika ini berhubungan dengan Tipe Data Boolean karena mengeluarkan output True or False
# Operator Logika : AND, OR
# Penggunaan AND : Jika kedua kondisi logika terpenuhi, maka outputnya True. Jika hanya 1 saja atau bahkan tidak ada maka outputnya adalah False
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print(" ")
# Operator Logika : OR
# Jika Ada Kedua ata Satu kondisi logika yang terpenuhi, maka outputnya True. Jika tidak ada yang terpenuhi, maka outputnya False
print(True or True)
print(True or False)
print(False or True)
print(False or False)