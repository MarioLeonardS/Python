# Jenis - jenis operasi perbandingan di Python
# Untuk output dari operasi perbandingan adalah Boolean atau (True/False)
# Operasi perbandingan juga bisa dilakukan ke tipe data String/text

# > lebih dari
# < kurang dari
# >= lebih dari sama dengan
# <= kurang dari sama dengan
# == Sama dengan
# != tidak sama dengan

# Contoh
print(1>4) # Jawabannya akan menjadi False

print(1<4) # Jawabannya akan menjadi True

print(1<1) # Jawabannya akan menjadi False, karena 1 = 1

print(1<=1) # Jawabannya akan menjadi True, karena 1 = 1

print(2>=1) # Jawabannya akan menjadi True, karena 2 > 1

print(3<=1) # Jawabannya akan menjadi False, ksrena 3 > 1 dan != 1

print(1==1) # Jawabannya akan menjadi True, karena 1 = 1

print(1!=2) # Jawabannya akan menjadi True, karena 1 tidak sama dengan 2

